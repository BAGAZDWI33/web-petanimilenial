                        <h1 class="mb-4">Teknologi Pengendalian Hama dan Penyakit</h1>
                        <p>
                            Salah satu metode pengendalian hama tanaman adalah
                            dengan teknik budidaya. Penggunaan mulsa dan pupuk dapat
                            berpengaruh terhadap perkembangan hama pada tanaman. Efek
                            individu dari mulsa signifikan pada pengurangan intensitas
                            serangan S. exigua dan meningkatkan hasil bawang merah.
                            Mulsa jerami padi mengurangi intensitas serangan S. exigua
                            sebesar 20.83%, sedangkan mulsa kakao meningkatkan hasil
                            sebesar 47.61% dibandingkan dengan perlakuan kontrol
                            (Valentino dan Thaha, 2019). Penggunaan ekstrak biji
                            dan daun tanaman Mimba mampu menekan laju serangan
                            Liriomyza spp. hingga 3.04% dan meningkatkan produktivitas
                            BMVLP (Arfan et al., 2019). Hasil penelitian Agustin et al.,
                            (2016) menunjukkan bahwa, ekstrak daun mimba konsentrasi
                            1% lebih efektif menekan pertumbuhan A. porri dengan daya
                            hambat 43.33%.
                        </p>
                        <p> Penggunaan jamur Beauveria bassiana untuk mengendalikan
                            serangan Spodoptera exigua Hubner sudah dilakukan pada
                            varietas bawang merah lembah palu (Allium wakegi). Penelitian
                            yang dilakukan pada tahun 2014 menunjukkan bahwa aplikasi
                            jamur Beuveria bassiana secara signifikan mempengaruhi
                            intensitas serangan S.exigua, selama 4-8 minggu setelah tanam
                            (MST). Perlakuan B. Bassiana 10 g L-1 air dengan interval
                            penyemprotan 5 hari sekali, mampu mengendalikan serangan S.exigua dengan hasil terendah
                            (1-2%) serta menghasilkan bobot umbi
                            tertinggi (143.82 g rumpun-1) atau 3.60 ton ha-1 dibandingkan dengan
                            semua perlakuan lainnya (Roziyanto et al., 2013). Aplikasi Biverin
                            dengan bahan aktif Beauveria bassiana, dengan konsentrasi 6 ml L-1
                            air yang diaplikasikan setiap 5 hari, lebih efektif menekan kepadatan
                            dan tingkat serangan populasi larva S. exiqua. Disamping itu, dapat
                            menghasilkan bawang merah sebesar 8.53 ton ha-1 (Abd. Razak et
                            al., 2016). Aplikasi biokultur diperkaya bioinsektisida B. bassiana
                            dapat mengendalikan hama S. exigua pada pertanaman BMVLP.
                            Aplikasi biokultur diperkaya bioinsektisida B. bassiana sebesar 10
                            g L-1 air dapat menurunkan serangan hama ulat bawang S. exigua
                            sebesar 10% dan meningkatkan sebesar 3.96 ton ha-1 dibandingkan
                            dengan kontrol (Nasir et al., 2017). </p>

                        <div class="col-md-6">
