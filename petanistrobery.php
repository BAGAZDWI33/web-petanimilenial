        <script>
            $(document).ready(function() {
                $('#commentForm').submit(function(event) {
                    event.preventDefault();

                    // Ambil nilai dari input
                    const name = $('input[name="name"]').val();
                    const email = $('input[name="email"]').val();
                    const avatar = $('#avatar')[0].files[0]; // Ambil file foto yang diunggah
                    const comment = $('textarea[name="comment"]').val();

                    // Simulasi upload foto (harus diganti dengan upload ke server)
                    let photoUrl = 'img/user.jpg'; // Default user image
                    if (avatar) {
                        // Jika ada file foto yang diunggah, bisa menggunakan URL.createObjectURL
                        photoUrl = URL.createObjectURL(avatar);
                    }

                    // Buat elemen komentar baru
                    const newComment = `
                    <div class="d-flex mb-4">
                        <img src="${photoUrl}" class="img-fluid" style="width: 45px; height: 45px;">
                        <div class="ps-3">
                            <h6><a href="#">${name}</a> <small><i>${getCurrentDate()}</i></small></h6>
                            <p>${comment}</p>
                            <button class="btn btn-sm btn-primary">Reply</button>
                        </div>
                    </div>
                `;

                    // Masukkan komentar baru ke dalam daftar komentar
                    $('#commentList').prepend(newComment);

                    // Reset form setelah mengirim komentar
                    $('#commentForm')[0].reset();
                });

                // Fungsi untuk mendapatkan tanggal sekarang dalam format yang diinginkan
                function getCurrentDate() {
                    const date = new Date();
                    return `${date.getDate()} ${getMonthName(date.getMonth())} ${date.getFullYear()}`;
                }

                // Fungsi untuk mendapatkan nama bulan dari indeks bulan
                function getMonthName(monthIndex) {
                    const months = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun', 'Jul', 'Aug', 'Sep', 'Oct', 'Nov',
                        'Dec'
                    ];
                    return months[monthIndex];
                }
            });
        </script>
